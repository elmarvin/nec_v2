public class PerfilSuelo {
    String A,B,C,D,E,F;

    public PerfilSuelo(String a, String b, String c, String d, String e, String f) {
        A = a;
        B = b;
        C = c;
        D = d;
        E = e;
        F = f;
    }

    public String getA() {
        return A;
    }

    public void setA(String a) {
        A = a;
    }

    public String getB() {
        return B;
    }

    public void setB(String b) {
        B = b;
    }

    public String getC() {
        return C;
    }

    public void setC(String c) {
        C = c;
    }

    public String getD() {
        return D;
    }

    public void setD(String d) {
        D = d;
    }

    public String getE() {
        return E;
    }

    public void setE(String e) {
        E = e;
    }

    public String getF() {
        return F;
    }

    public void setF(String f) {
        F = f;
    }
}
